    private void LlenarComboBox() {
        Roles r = new Roles();
        RolesDAO rDAO = new RolesDAO();
        ArrayList<Roles> llenarComboBox = rDAO.ListarRoles();

        for (int i = 0; i < llenarComboBox.size(); i++) {
            cbRol.addItem(new Roles(llenarComboBox.get(i).getIdroles(), llenarComboBox.get(i).getRol()));
        }
    }

    public void GuardarCliente() {
        Trabajadores trab = new Trabajadores();
        TrabajadoresDAO trabDao = new TrabajadoresDAO();

        if (!"".equals(txtNombre.getText()) || !"".equals(txtTelefono.getText()) || !"".equals(txtCorreo.getText()) || !"".equals(txtUsuario.getText()) || !"".equals(txtClave.getText()) || !"".equals(cbRol.getSelectedIndex())) {
            trab.setNombre(txtNombre.getText());
            trab.setTelefono(Long.parseLong(txtTelefono.getText()));
            trab.setCorreo(txtCorreo.getText());
            trab.setUsuario(txtUsuario.getText());
            trab.setClave(txtClave.getText());
            trab.setIdrol(cbRol.getItemAt(cbRol.getSelectedIndex()).getIdroles());
            trabDao.RegistrarTrabajador(trab);

            JOptionPane.showMessageDialog(null, "El usuario ha sido registrado con exito", "Registro Exitoso", JOptionPane.INFORMATION_MESSAGE);
            LimpiarCampos();
        } else {
            JOptionPane.showMessageDialog(null, "No se permiten campos vacios, si no conoce los datos escriba N/A", "Formulario Incompleto", JOptionPane.WARNING_MESSAGE);
        }
    }
    
    private void LimpiarCampos(){
        txtNombre.setText("");
        txtTelefono.setText("");
        txtCorreo.setText("");
        txtUsuario.setText("");
        txtClave.setText("");
    }

